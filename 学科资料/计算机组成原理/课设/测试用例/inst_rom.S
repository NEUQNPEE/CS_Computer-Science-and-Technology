.org 0x0
.set noat
.set noreorder
.global _start
_start:
    lui $1,0x1234         #$1=0x12340000
    lui $2,0x5678         #$2=0x56780000
    nop
    nop        #c
s1:
    add $3,$1,$2          #$3=0x68ac0000
    sub $4,$1,$2          #$4=0xbbbc0000
    or $5,$1,$2           #$5=0x567c0000
    and $6,$1,$2 #1c      #$6=0x12300000
    xor $7,$1,$2          #$7=0x444c0000
    nor $8,$1,$2          #$8=0xa983ffff
    bne $1,$0,s2          #如果$1和$0不相等则跳转到s2
    nop          #2c
    lui $1,0xf760 
    lui $2,0x1234 
s2:
    nop
    nop         #3c
    beq $2,$1,s1             #如果$2和$1相等则跳转到s1
    nop
    slt $9,$1,$2             #$9 =0x00000001
    sltu $10,$1,$2 #4c        $10=0x00000001
    sll $11,$1,2             #$11=0x48d00000 
    srl $12,$1,16            #$12=0x00001234
    sra $13,$1,2             #$13=0x048d0000
    addu $14,$1,$2   #5c      $14=0x68ac0000
    addiu $15,$1,0x2020      #$15=0x12342020
    subu $16,$1,$2           #$16=0xbbbc0000
    sltu $17,$1,$2           #$17=0x00000001
    sw $14,0x8($0)   #6c     #将$14的值(0x68ac0000)存入物理地址为0x00000008的字
    lw $18,0x8($0)           #$18=0x68ac0000
    jal 0x80                 #$31=0x0000007c
    nop
    addiu $1,$0,0x2020         #$1=0x00002020
    addiu $2,$0,0x2010  #80     $2=0x00002010
    addiu $3,$0,0x00a0         #$3=0x000000a0
    nop
    nop
    jr $3  #90               #跳转到$3所指位置
    nop   
    add $3,$1,$2             
    sub $4,$1,$2 
    or $5,$1,$2    #a0      #jr target 0xa0  
    lui $31,0x0101          #$31=0x01010000
finish:
    nop
    nop
   

    
